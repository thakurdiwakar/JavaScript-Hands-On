const operations = {
  add(a, b) {
    return a + b;
  },
  sub(a, b) {
    return a - b;
  },
  mul(a, b) {
    return a * b;
  },
  div(a, b) {
    return a / b;
  },
};

console.log(operations.add(2, 3));
console.log(operations.sub(2, 3));
console.log(operations.mul(2, 3));
console.log(operations.div(6, 3));
